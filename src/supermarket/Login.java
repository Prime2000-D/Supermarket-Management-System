    private void LoginBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LoginBtnMouseClicked
        // TODO add your handling code here:
        if(RoleCb.getSelectedItem().toString().equals("SELLER"))
        {
            String Query = "Select * From supermarketdb.sellertbl where SellName= '" + Uid.getText()+"' and SellPass='"+password.getText()+"'";
            try {
                 Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/supermarketdb","root","Secret@2181");
                St = Con.createStatement();
                Rs = St.executeQuery(Query);
                if(Rs.next()){
                    new Selling().setVisible(true);
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Wrong Seller Id or Password");
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }else{
            
            String Query = "Select * From supermarketdb.admintbl where AdminName= '" + Uid.getText()+"' and AdminPass='"+password.getText()+"'";
            try {
                 Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/supermarketdb","root","Secret@2181");
                St = Con.createStatement();
                Rs = St.executeQuery(Query);
                if(Rs.next()){
                    new Products().setVisible(true);
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Wrong Admin Id or Password");
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_LoginBtnMouseClicked

